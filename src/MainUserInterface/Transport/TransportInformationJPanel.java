    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handlinglblErrorFnamere:
        if ((lblErrorFname.getText() == null || lblErrorFname.getText().equals(""))
            && (lblErrorLname.getText() == null || lblErrorLname.getText().equals(""))
            && (lblErrorAge.getText() == null || lblErrorAge.getText().equals(""))
            && (lblErrorVNumber.getText() == null || lblErrorVNumber.getText().equals(""))) {
            Transport bus = new Transport();
            bus.setDriverFirstName(txtFirstName.getText());
            bus.setDriverLastName(txtLastName.getText());
            bus.setDriverAge(Integer.parseInt(txtAge.getText()));
            bus.setVehicleNo(txtVehicleNumber.getText());
            bus.setStatus("Bus Available");
            ecoSystem.getTransportDirectory().createBus(bus);
               
            JOptionPane.showMessageDialog(null, "Profile Created");

            TransportWorkAreaJPanel ambulanceWorkAreaJPanel = new TransportWorkAreaJPanel(userProcessContainer, userAccount, ecoSystem);
            userProcessContainer.add("Add Student", ambulanceWorkAreaJPanel);
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            layout.next(userProcessContainer);
        } else {
            JOptionPane.showMessageDialog(null, "Please Fill All Details Correctly");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed


